class Solution {
  public:
    // Function returns the second
    // largest elements
    int getSecondLargest(vector<int> &arr) {
        int lar=-1, s_lar=-1;
        for(auto it:arr){
            if(it>lar){
                s_lar=lar;
                lar=it;
            }else if(it<lar&&it>s_lar){
                s_lar=it;
            }
        }
        return s_lar;
    }
};

