// User function template for C++
class Solution {
  public:
    int rowWithMax1s(vector<vector<int>> &arr) {
        int row=arr.size(),col=arr[0].size();
        vector<int>v(row);
        int maxi=-1;
        for(int r=0;r<row;r++){
            int cntOne=0;
            for(int c=0;c<col;c++){
                if(arr[r][c]==1){
                    cntOne++;
                }
            }
            v[r]=cntOne;
        }
        for(auto it:v){
            if(it>maxi){
                maxi=it;
            }
        }
        for(int i=0;i<row;i++){
            if(v[i]==maxi&&v[i]!=0){
                return i;
            }
        }
        return -1;
    }
};

