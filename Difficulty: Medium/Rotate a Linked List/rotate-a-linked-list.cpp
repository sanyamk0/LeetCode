class Solution {
  public:
    Node* rotate(Node* head, int k) {
        vector<int> arr;
        Node* temp = head ;
        while(temp != NULL){
            arr.push_back(temp -> data);
            temp = temp ->next;
        }
        int n = arr.size();
        k =  k % n;
        reverse(arr.begin(), arr.begin() + k);
        reverse(arr.begin() + k , arr.end());
        reverse(arr.begin(), arr.end());
        Node* head2 = new Node(arr[0]);
        Node * newele = head2;
        for(int i = 1; i < n; i++){
             newele -> next = new Node(arr[i]);
             newele = newele -> next;
        }
        return head2;
    }
};


